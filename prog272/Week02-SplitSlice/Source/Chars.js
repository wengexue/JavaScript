exports.chars = {
	
	getComma: function() {
		'use strict';
		return ',';
	},
	
	getTab: function() {
		'use strict';
		return '\t';
	},
	getNewLine: function() {
		'use strict';
		return '\n';
	},
	getDoubleQuote: function() {
		'use strict';
		return '"';
	},
	getHyphen: function() {
		'use strict';
		return '-';
	}

};
